// Object
// An object in JavaScript is a data structure used to store collections of key-value pairs. Objects can be created using the Object constructor or through object literals.

// Creating an Object Using the Object Constructor
// javascript
// Copy code
// let person = new Object();
// person.name = 'John';
// person.age = 30;
// person.greet = function() {
//     console.log('Hello, ' + this.name);
// };
// Key Characteristics
// Dynamic Properties: Properties can be added or removed dynamically after the object is created.
// Inheritance: Objects can inherit properties and methods from other objects through prototypes.

// Object Literal
// An object literal is a shorthand way to create objects in JavaScript using a simple and readable syntax.

// Creating an Object Using an Object Literal
// javascript
// Copy code
// let person = {
//     name: 'John',
//     age: 30,
//     greet: function() {
//         console.log('Hello, ' + this.name);
//     }
// };
// Key Characteristics
// Concise Syntax: Object literals provide a more concise and readable way to create objects compared to using the Object constructor.
// Initialization: Properties and methods are defined at the time of object creation.